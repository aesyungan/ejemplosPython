def suma(val1, val2):
    """suma dos valores"""
    return val1+val2


def resta(val1, val2):
    """resta dos valores"""
    return val1-val2


def divicion(val1, val2):
    """divide dos valores"""
    return val1/val2


def multiplicacion(val1, val2=1):
    """multiplica dos valores"""
    return val1*val2
